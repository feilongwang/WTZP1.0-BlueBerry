/***********************************************************************
** 函 数 名： Get_CO2()
** 函数说明： 设置AD口
**---------------------------------------------------------------------
** 输入参数： 二氧化碳浓度测量
** 返回参数： 二氧化碳浓度（ppm)
***********************************************************************/
uint16 Get_CO2()
{
   uint16 temp;
   temp=GetAD(0x02);
   return 2.4414*temp;
}